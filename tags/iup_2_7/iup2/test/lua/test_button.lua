-- creates a text box
text = iuptext{ readonly = "YES", SIZE = "EIGHTH" }


-- creates a button with image
btn_image = iupbutton{ title = "Button with image", image = img_release, impress = img_press, iminactive = img_inactive }


-- creates a button entitled Activate
btn_on_off = iupbutton{ title = "Activate" }


-- creates a dialog and sets dialog's title
dlg = iupdialog
{
   iupvbox
   {
      iuphbox
      {
         btn_image,
         btn_on_off, 
      },
      text;
      margin = "10x10"
   };
   title = "IupButton"
}


-- callback called when activate button is activated
function btn_on_off:action()
  if btn_image.image == img_release then
    btn_image.image = img_press
    btn_image.pressed = IUP_YES
  else
    btn_image.image = img_release
    btn_image.pressed = IUP_NO
  end
end


-- callback called when the button is pressed or released
function btn_image:button( b, e )
  if( b == IUP_BUTTON1 ) then
    if( e == 1 ) then
    -- botão pressionado
      text.value = "Red button pressed"
    else           
    -- botão solto 
      text.value = "Red button released"
    end
  end
end

-- shows dialog
dlg:showxy( IUP_CENTER, IUP_CENTER) 
