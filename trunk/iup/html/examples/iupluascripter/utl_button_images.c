int total_images = 0;
Ihandle *images[100];

void utlCreateIupImage(int w, int h, unsigned char *bits, char *colors[], char *name)
{
  char c[4];
  int i;
  Ihandle* iup_img = IupImage(w, h, bits);

  IupSetHandle(name, iup_img);
  for(i=0; colors[i] != NULL; i++)
  {
    sprintf(c, "%d", i);
    IupSetAttribute(iup_img,c,colors[i]);
  }
  images[total_images] = iup_img;
  total_images++;
}

